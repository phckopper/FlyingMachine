void start_mavlink(void) {
	HAL_UART_Receive_DMA(&huart2, mav_buffer, sizeof(mav_buffer));
}

void process_buffer(uint8_t *tmp, uint32_t count) {
	for(uint32_t i = 0; i < count; i++) {
		if (mavlink_parse_char(0, tmp[i], &msg, &status)) {
			switch (msg.msgid) {
			case MAVLINK_MSG_ID_HEARTBEAT: {
				HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_4);
				break;
			}
			case MAVLINK_MSG_ID_MANUAL_CONTROL: {
				mavlink_manual_control_t controls;
				mavlink_msg_manual_control_decode(&msg, &controls);
				pitch = controls.x;
				roll = controls.y;
				throttle = controls.z;
				yaw = controls.r;
				enabled = controls.buttons ? 1 : 0;
				break;
			}
			}
		}
	}
}
